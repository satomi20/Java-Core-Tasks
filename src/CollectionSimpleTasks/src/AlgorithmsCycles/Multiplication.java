package CollectionSimpleTasks.src.AlgorithmsCycles;

public class Multiplication {

    public static void main(String[] args) {
        int a = 3;
        int b = 20;
        int s = 0;
        do {
            s = a * b;
            System.out.println(s);
            break;
        }while (s > 0);
    }
}
