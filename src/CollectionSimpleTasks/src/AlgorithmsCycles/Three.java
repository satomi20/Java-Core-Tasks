//Операция с целым числом
public class Three {

    public static void main(String[] args) {
        int num1 = 5;
        if (num1>0) {
            System.out.println(num1 + 1);
        } else if (num1<0) {
            System.out.println(num1 - 2);
        }else if (num1==0) {
            System.out.println(num1 + 10);
        }
    }
}
